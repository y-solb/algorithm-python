# 3. 수 자료형

# 지수 표현 방식
# - e나 E를 이용한 지수 표현
# - e나 E 다음에 오는 수는 10의 지수부
# - 1e9라고 입력하면, 10의 9제곱(1,000,000,000)
# - 최단 경로 알고리즘에서 도달할 수 없는 노드에 대하여 최단 거리를 무한(INF)으로 설정

a=1e9
print(a)

b=75.25e1
print(b)

c=3954e-3
print(c)

x=7
y=3

# 나누기
print(x/y)

# 나머지
print(x%y)

# 몫
print(x//y)

# 거듭제곱
print(x**y)

# 제곱근
print(x**0.5)